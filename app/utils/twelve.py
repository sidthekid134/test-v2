def get_twelve():
    """
    Returns the integer 12.
    
    Returns:
        int: The value 12
    """
    return 12

def is_twelve(value):
    """
    Checks if a value equals 12.
    
    Args:
        value: The value to check
        
    Returns:
        bool: True if value equals 12, False otherwise
    """
    return value == 12

def twelves(count):
    """
    Generates a list containing 12 repeated 'count' times.
    
    Args:
        count (int): Number of times to repeat 12
        
    Returns:
        list: A list containing 12 repeated 'count' times
    """
    return [12] * count